def pair_distance_fractions_on_circle(n: int) -> np.ndarray:
    """Return sin(delta/2) for all unordered pairs of n points on a circle."""
    thetas = np.linspace(0.0, 2.0 * math.pi, n, endpoint=False)
    fracs: List[float] = []
    for i in range(n):
        for j in range(i + 1, n):
            dtheta = abs(thetas[j] - thetas[i])
            dtheta = min(dtheta, 2.0 * math.pi - dtheta)
            fracs.append(math.sin(dtheta / 2.0))
    return np.array(fracs, dtype=float)


def success_prob_hub(x: float, n: int, jitter_frac: float) -> float:
    """Exact success probability for hub exchange at normalised diameter x = D/(v*theta)."""
    slack = 1.0 - x
    if slack <= 0:
        return 0.0
    if jitter_frac <= 0:
        return 1.0
    F = gamma2_cdf(np.array([slack]), scale=jitter_frac)[0]
    return float(F**n)


def success_prob_all_to_all(x: float, n: int, jitter_frac: float, fracs: np.ndarray | None = None) -> float:
    """Exact success probability for all-to-all exchange at normalised diameter x = D/(v*theta)."""
    if fracs is None:
        fracs = pair_distance_fractions_on_circle(n)
    slack = 1.0 - 2.0 * x * fracs
    if np.any(slack <= 0):
        return 0.0
    if jitter_frac <= 0:
        return 1.0
    F = gamma2_cdf(slack, scale=jitter_frac)
    logp = np.sum(np.log(F))
    return float(np.exp(logp))


def compute_transition_curves(n: int, jitter_frac: float, xs: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    fracs = pair_distance_fractions_on_circle(n)
    p_hub = np.array([success_prob_hub(float(x), n, jitter_frac) for x in xs])
    p_all = np.array([success_prob_all_to_all(float(x), n, jitter_frac, fracs) for x in xs])
    return p_hub, p_all


def bisection_x50(prob_fn, lo: float, hi: float, target: float = 0.5, iters: int = 50) -> float:
    plo = prob_fn(lo)
    phi = prob_fn(hi)
    if not (plo >= target >= phi):
        return lo if abs(plo - target) < abs(phi - target) else hi
    for _ in range(iters):
        mid = 0.5 * (lo + hi)
        pm = prob_fn(mid)
        if pm >= target:
            lo = mid
        else:
            hi = mid
    return 0.5 * (lo + hi)


