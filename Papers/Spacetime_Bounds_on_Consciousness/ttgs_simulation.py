def gamma2_cdf(x: np.ndarray, scale: float) -> np.ndarray:
    """CDF of Gamma(k=2, scale=scale)."""
    x = np.asarray(x)
    out = np.zeros_like(x, dtype=float)
    if scale <= 0:
        out[x >= 0] = 1.0
        out[x < 0] = 0.0
        return out
    z = x / scale
    mask = x >= 0
    out[mask] = 1.0 - np.exp(-z[mask]) * (1.0 + z[mask])
    out[~mask] = 0.0
    return out


