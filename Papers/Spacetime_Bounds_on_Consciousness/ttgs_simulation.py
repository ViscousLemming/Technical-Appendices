@dataclass(frozen=True)
class PipelineParams:
    n_windows: int
    n_samples_v: int
    true_ratio: float
    sigma_D: float
    sigma_theta: float
    sigma_v: float


def v_estimator(v_samples: np.ndarray, mode: str) -> np.ndarray:
    """Estimate v per window. v_samples has shape (..., n_samples_v)."""
    if mode == "min":
        return np.min(v_samples, axis=-1)
    if mode == "mean":
        return np.mean(v_samples, axis=-1)
    if mode == "median":
        return np.median(v_samples, axis=-1)
    if mode == "q95":
        return np.quantile(v_samples, 0.95, axis=-1)
    if mode == "max":
        return np.max(v_samples, axis=-1)
    raise ValueError(f"Unknown mode {mode}")


def one_sided_t_pvalue_vec(M: np.ndarray) -> np.ndarray:
    """Vectorised one-sided t test for each row. M has shape (batch, n_windows)."""
    n = M.shape[1]
    mu = M.mean(axis=1)
    s = M.std(axis=1, ddof=1)
    # Avoid divide by zero.
    s = np.where(s == 0.0, np.inf, s)
    t = mu / (s / math.sqrt(n))
    return stats.t.sf(t, df=n - 1)


def noisy_positive(true_value, sigma: float, rng: np.random.Generator, size) -> np.ndarray:
    """Multiplicative Gaussian noise clipped to stay positive.

    sigma is a fractional standard deviation. For example sigma=0.05 corresponds
    to 5% relative noise.
    """
    y = true_value * (1.0 + rng.normal(0.0, sigma, size=size))
    return np.maximum(y, 1e-12)


def rejection_rate(
    params: PipelineParams,
    v_mode: str,
    n_rep: int,
    alpha: float,
    rng: np.random.Generator,
    batch_size: int = 800,
) -> float:
    """Monte Carlo rejection probability with vectorised batches."""
    v_true = 1.0
    theta_true = 1.0
    D_true = params.true_ratio * v_true * theta_true

    remaining = n_rep
    rej = 0
    while remaining > 0:
        b = min(batch_size, remaining)
        D_hat = noisy_positive(D_true, params.sigma_D, rng, size=(b, params.n_windows))
        theta_hat = noisy_positive(theta_true, params.sigma_theta, rng, size=(b, params.n_windows))
        v_samples = noisy_positive(v_true, params.sigma_v, rng, size=(b, params.n_windows, params.n_samples_v))
        v_hat = v_estimator(v_samples, v_mode)
        M = D_hat - v_hat * theta_hat
        p = one_sided_t_pvalue_vec(M)
        rej += int(np.sum(p < alpha))
        remaining -= b
    return rej / n_rep


def rejection_rate_multi(
    params: PipelineParams,
    v_modes: list[str],
    n_rep: int,
    alpha: float,
    rng: np.random.Generator,
    batch_size: int = 800,
) -> dict[str, float]:
    """Monte Carlo rejection probability for multiple v estimators.

    All estimators are evaluated on the same synthetic draws so that differences
    reflect the estimator choice rather than Monte Carlo noise.
    """

    v_true = 1.0
    theta_true = 1.0
    D_true = params.true_ratio * v_true * theta_true

    counts = {m: 0 for m in v_modes}
    remaining = n_rep
    while remaining > 0:
        b = min(batch_size, remaining)
        D_hat = noisy_positive(D_true, params.sigma_D, rng, size=(b, params.n_windows))
        theta_hat = noisy_positive(theta_true, params.sigma_theta, rng, size=(b, params.n_windows))
        v_samples = noisy_positive(v_true, params.sigma_v, rng, size=(b, params.n_windows, params.n_samples_v))

        for m in v_modes:
            v_hat = v_estimator(v_samples, m)
            M = D_hat - v_hat * theta_hat
            p = one_sided_t_pvalue_vec(M)
            counts[m] += int(np.sum(p < alpha))

        remaining -= b

    return {m: counts[m] / n_rep for m in v_modes}


