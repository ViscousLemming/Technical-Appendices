def figure_literature_anchor(out_dir: Path, source_dir: Path, primate_csv: Path) -> None:
    # Conservative human brain diameter derived from the human brain masses in
    # Table 1 of Phillips et al. Convert mass to an equivalent-sphere diameter
    # by assuming density 1 g/cm^3, then we pad by ±5% to avoid underestimating
    # diameter.
    human_masses: list[float] = []
    with primate_csv.open("r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row.get("species") == "Homo sapiens":
                human_masses.append(float(row["brain_mass_g"]))
    if len(human_masses) == 0:
        raise RuntimeError("No Homo sapiens rows found in primate CSV")
    human_masses_g = np.array(human_masses, dtype=float)
    vol_cm3 = human_masses_g / DENSITY_G_PER_CM3
    r_cm = (3.0 * vol_cm3 / (4.0 * np.pi)) ** (1.0 / 3.0)
    D_sphere_m = 2.0 * r_cm / 100.0
    pad = DIAMETER_PADDING_FRAC
    D_lo = float(D_sphere_m.min() * (1.0 - pad))
    D_hi = float(D_sphere_m.max() * (1.0 + pad))

    # Callosal conduction velocities summarised by Innocenti et al.
    v_lo, v_hi = CALLOSAL_V_LO_M_PER_S, CALLOSAL_V_HI_M_PER_S

    n_samples = 100000
    rng = np.random.default_rng(SEED + 1)
    D = rng.uniform(D_lo, D_hi, size=n_samples)
    v = rng.uniform(v_lo, v_hi, size=n_samples)

    theta_hub = D / (1.0 * v)
    theta_all = D / (0.5 * v)

    fig = plt.figure(figsize=(6.0, 3.6))
    ax = fig.add_subplot(1, 1, 1)
    ax.hist(theta_hub * 1000.0, bins=80, density=True, alpha=0.7, label="Hub kappa=1")
    ax.hist(theta_all * 1000.0, bins=80, density=True, alpha=0.7, label="All-to-all kappa=0.5")
    ax.set_xlabel(r"Implied lower bound $\theta_{\min}$ (ms)")
    ax.set_ylabel("Density")
    ax.legend(frameon=False)
    save_pdf(fig, out_dir / "ttgs_literature_anchor.pdf")

