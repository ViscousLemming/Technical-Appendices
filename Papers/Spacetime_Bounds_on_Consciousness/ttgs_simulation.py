def sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        while True:
            chunk = f.read(1024 * 1024)
            if not chunk:
                break
            h.update(chunk)
    return h.hexdigest()


def write_manifest(out_dir: Path) -> Path:
    source_dir = out_dir / "ttgs_source_data"
    paths: List[Path] = []
    for pattern in ["ttgs_*.pdf", "ttgs_*.tex", "ttgs_*.csv"]:
        paths.extend(sorted(out_dir.glob(pattern)))
    if source_dir.exists():
        paths.extend(sorted(source_dir.glob("*.csv")))
    script_path = out_dir / "ttgs_simulation.py"
    if script_path.exists():
        paths.append(script_path)
    provenance_path = out_dir / "ttgs_provenance.json"
    if provenance_path.exists():
        paths.append(provenance_path)
    # Deduplicate while preserving deterministic order.
    uniq: List[Path] = []
    seen: set[str] = set()
    for p in paths:
        key = str(p.relative_to(out_dir))
        if key in seen:
            continue
        seen.add(key)
        uniq.append(p)

    files: Dict[str, Dict[str, object]] = {}
    for p in uniq:
        rel = str(p.relative_to(out_dir))
        st = p.stat()
        files[rel] = {
            "bytes": int(st.st_size),
            "sha256": sha256_file(p),
        }

    manifest = {
        "package_version": PACKAGE_VERSION,
        "license_spdx": LICENSE_SPDX,
        "copyright_holder": COPYRIGHT_HOLDER,
        "copyright_year": COPYRIGHT_YEAR,
        "license_file": "LICENSE",
        "notice_file": "NOTICE",
        "generated_at_utc": generated_at_utc_iso(),
        "source_date_epoch": source_date_epoch(),
        "python": sys.version.split()[0],
        "numpy": np.__version__,
        "scipy": scipy.__version__,
        "matplotlib": plt.matplotlib.__version__,
        "files": files,
    }
    manifest_path = out_dir / "ttgs_manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2, sort_keys=True), encoding="utf-8")
    return manifest_path


