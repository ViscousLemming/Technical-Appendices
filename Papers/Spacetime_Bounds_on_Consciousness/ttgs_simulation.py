def bootstrap_slope_and_r2(x: np.ndarray, y: np.ndarray, n_boot: int, seed: int) -> Tuple[float, float, float, float]:
    """Bootstrap CI for the log log slope and return R^2 of the original fit.

    x and y must be positive.
    """
    lx = np.log(x)
    ly = np.log(y)

    slope, intercept, r_value, _, _ = stats.linregress(lx, ly)
    r2 = r_value**2

    rng = np.random.default_rng(seed)
    n = len(x)

    # Vectorised bootstrap. idx has shape (n_boot, n).
    idx = rng.integers(0, n, size=(n_boot, n))
    lx_s = lx[idx]
    ly_s = ly[idx]

    mx = lx_s.mean(axis=1)
    my = ly_s.mean(axis=1)
    dx = lx_s - mx[:, None]
    dy = ly_s - my[:, None]
    sxy = np.sum(dx * dy, axis=1)
    sxx = np.sum(dx * dx, axis=1)

    # Guard against degenerate samples with zero variance.
    slopes = np.where(sxx == 0.0, np.nan, sxy / sxx)
    slopes = slopes[np.isfinite(slopes)]

    lo = float(np.quantile(slopes, 0.025))
    hi = float(np.quantile(slopes, 0.975))
    return float(slope), lo, hi, float(r2)


